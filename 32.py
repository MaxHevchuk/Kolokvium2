# 32. Змінній t привласнити значення істина, якщо в одновимірному масиві є хоча б одне від’ємне і парне число.
# Гевчук Максим КН-А

arr = list(map(int, input('Введіть елементи масиву через пробіл:\n>>> ').split()))  # введення
t = True if list(filter(lambda x: x < 0 and x % 2 == 0, arr)) else False  # перевірка умови
print(t)  # виведення
