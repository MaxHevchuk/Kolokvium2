# 36. Знайти суму додатніх елементів лінійного масиву цілих чисел.
# Розмірність масиву - 10. Заповнення масиву здійснити з клавіатури.
# Гевчук Максим КН-А

arr = [int(input(f'{i + 1}-ий елемент: ')) for i in range(10)]  # введення елементів масиву
total = sum(tuple(filter(lambda x: x > 0, arr)))  # фільтр та сума чисел, що більші за нуль
print(f'Сума додатніх елементів масиву: {total}')  # виведення
