# 48. Наведено список прізвищ брокерів товарної біржі з N чоловік. Поміняйте місцями прізвища брокерів:
# першого і останнього, другого і передостаннього, третього з початку і третього від кінця і т.д.
# Гевчук Максим КН-А

surnames = [input(f'{i + 1}. ') for i in range(int(input('Введіть к-сть брокерів:\n>>> ')))]  # введення прізвищ
print(f'Змінений список прізвищ:\n{" ".join(reversed(surnames))}')  # виведення "перевернутого" списку
