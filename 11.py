# 11. Дані про температуру води на Чорноморському узбережжі за декаду
# вересня зберігаються в масиві. Визначити, скільки за цей час було днів, придатних для
# купання.
# Гевчук Максим КН-А

arr = tuple(map(float, input('Введіть масив даних про температуру через пробіл:\n>>> ').split()))  # введення масиву
count = len(list(filter(lambda x: 22 <= x <= 24, arr)))  # фільтр та підрахунок температур у діапазоні [22; 24]
print(f'Кількість придатнх днів для купання: {count}')  # виведення
