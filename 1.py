# Введіть з клавіатури в масив п'ять цілочисельних значень. Виведіть їх в
# один рядок через кому. Отримайте для масиву середнє арифметичне.
# Гевчук Максим група 122А

import numpy as np

arr = np.zeros(5, dtype=int)
for i in range(5):
    arr[i] = int(input(f'Елемент {i + 1} >>> '))
print(f"\n{', '.join((map(str, arr)))}")
print(f'Середнє арифметичне = {np.mean(arr)}')
