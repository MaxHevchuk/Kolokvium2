# 59. Дан одновимірний масив з 10 цілих чисел. Підрахуйте кількість різних чисел в ньому.
# Гевчук Максим КН-А

from random import randint

arr = [randint(1, 10) for i in range(10)]  # створення масиву чисел розміром 10
print(*arr)  # виведення масиву
print(f'Різних чисел: {len((set(arr)))}, а саме:')  # виведення к-сті різних чисел
print(*set(arr))  # виведення різних чисел
