# 24. Знайти суму елементів масиву цілих чисел, які діляться на 5 і на 8 одночасно.
# Розмірність масиву - 30. Заповнення масиву здійснити випадковими числами від 500 до 1000.
# Гевчук Максим КН-А

from random import randint

arr = [randint(500, 1000) for i in range(30)]  # створення масиву
total = sum(list(filter(lambda x: x % 5 == 0 and x % 8 == 0, arr)))  # фільтр та сума чисел за умовою
# виведення
print(*arr)
print(f'Сума: {total}')
