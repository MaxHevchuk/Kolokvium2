# 40. Обчислити суму парних елементів одновимірного масиву до першого
# зустрінутого нульового елемента.
# Гевчук Максим КН-А

arr = list(map(int, input("Введіть елементи через пробіл:\n>>> ").split()))  # введення масиву чисел
total = sum(filter(lambda x: x % 2 == 0, arr[:arr.index(0)]))  # фільтр та сума елементів до першого нуля
print(f'Cума перших парних елементів до першого нуля: {total}')  # виведення
