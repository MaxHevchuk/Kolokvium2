import random

arr = [random.randint(0, 10) for i in range(7)]  # створення масиву розміром 7 з рандомними елементами
print(f'Масив з елементами:\n{arr}')
print(f'Масив з подвоєними елементами:\n{list(map(lambda x: 2 * x, arr))}')  # піднесення кожного елементу у квадрат
