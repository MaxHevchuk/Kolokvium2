# 26. Напишіть програму аналізу значень температури хворого за добу: визначте мінімальне і максимальне значення,
# середнє арифметичне. Заміри температури виробляються шість раз на добу і результати вводяться з клавіатури у масив T.
# Гевчук Максим КН-А

t = list(map(float, input('Введіть значення температури через пробіл:\n>>> ').split()))
print(f'Мінімальне значення: {min(t)}\n'
      f'Максимальне значення: {max(t)}\n'
      f'Середнє арифметичне: {sum(t) / len(t)}')
