from math import sqrt

arr = [int(input(f'Елемент {i + 1} >>> ')) for i in range(5)]  # створення масиву
for j in range(5):
    print(f'\nКорінь {arr[j]} = {sqrt(arr[j]):.2f}\n'  # вивід значення кореню кожного елементу
          f'Квадрат {arr[j]} = {arr[j] ** 2}')  # та квадратів
