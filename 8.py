# 8. Створіть цілочисельний масив А [1..15] за допомогою генератора
# випадкових чисел з елементами від -15 до 30 і виведіть його на екран. Визначте
# найбільший елемент масиву і його індекс.
# Гевчук Максим КН-А
import random

arr = [random.randint(-15, 30) for i in range(15)]
max_number = max(arr)
print(f'Масив з елементами:\n{arr}')
print(f'Найбільший елемент: {max_number}\n'
      f'Його індекс: {arr.index(max_number)}')
