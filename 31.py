# 31. Обчислити середнє арифметичне значення тих елементів одновимірного масиву, які потрапляють в інтервал від -2 до 10.
# Гевчук Максим КН-А

# введення елементів
arr = list(filter(lambda x: x in range(-2, 11), list(map(float, input('Введіть елем. через пробіл:\n>>> ').split()))))
print(f'Середнє арифметичне чисел у діапазоні [-2; 10]: {sum(arr) / len(arr)}')  # виведення
