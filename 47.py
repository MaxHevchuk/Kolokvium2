# 47. У лінійному масиві знайти максимальний елемент. Вставте порядковий номер елемента за ним, пересунувши всі
# залишилися на одну позицію вправо.
# Гевчук Максим КН-А

arr = list(map(int, input('Введіть масив через пробіл:\n>>> ').split()))  # введення елементів масиву
arr.insert(arr.index(max(arr)) + 1, arr.index(max(arr)))  # додавання індексу після максимального числа
print(f'Змінений масив:\n{" ".join(map(str, arr))}')  # виведення
