# 46. Задана таблиця назв товарів, що випускаються заводом. Визначте, чи повторюється в цій
# таблиці назва першого товару, і, якщо повторюється, видаліть назву першого товару з таблиці.
# Гевчук Максим КН-А

arr = ['Техніка', 'Одяг', 'Сумки', 'Канцтовари', 'Косметика', 'Техніка', 'Меблі', 'Взуття', 'Транспорт']
print(*arr)
if arr.count(arr[0]) > 1:  # якщо товар повторяється більше ніж 1 раз, то видаляється перша назва
    arr.pop(0)
print(*arr)
