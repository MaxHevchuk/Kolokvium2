# 6. Створіть масив А [1..8] за допомогою генератора випадкових чисел з
# елементами від -10 до 10 і виведіть його на екран. Підрахуйте кількість від’ємних
# елементів масиву.
# Гевчук Максим КН-А
import numpy as np

arr = np.array([np.random.randint(-10, 10) for i in range(8)])
count = 0
for item in arr:
    if item < 0:
        count += 1
print(f'Масив з елементами:\n{arr}')
print(f'Кількість від\'ємних елементів:\n{count}')
