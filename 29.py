# 29. Знайти кількість парних елементів одновимірного масиву до першого зустрінутого числа рівного наперед заданому числу а.
# Гевчук Максим КН-А

arr = list(map(int, input('Введіть елементи масиву:\n>>> ').split()))  # введення елементів
print(
    f'Кількість парних елементів до заданого числа: {len(list(filter(lambda x: x % 2 == 0, arr))[:arr.index(int(input("a: ")))])}')  # виведення
