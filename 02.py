# екран значення коріння і квадратів кожного з елементів масиву.
# Гевчук Максим КН-А
from math import sqrt

arr = []  # створення масиву
for i in range(5):
    arr.append(int(input(f'Елемент {i + 1} >>> ')))  # введення елементів масиву
for j in range(5):
    print(f'\nКорінь {arr[j]} = {sqrt(arr[j]):.2f}\n'  # вивід значення кореню кожного елементу
          f'Квадрат {arr[j]} = {arr[j] ** 2}')  # та квадратів
