# 15. Знайти суму парних елементів масиву цілих чисел. Розмірність масиву -20.
# Заповнення масиву здійснити випадковими числами від 100 до 200.
# Гевчук Максим КН-А

from random import randint

arr = [randint(100, 200) for i in range(20)]
total = sum(tuple(filter(lambda x: x % 2 == 0, arr)))
print(f'Масив елементів:\n{arr}\n'
      f'Сума парних елементів:\n{total}')